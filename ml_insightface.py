import matplotlib.pyplot as plt

def show_retrieval(query_filename, results, query_folder, gallery_folder, top_k=5):
    retrieved = results[query_filename][:top_k]

    plt.figure(figsize=(3 * (top_k + 1), 4))

    # Query image
    query_path = os.path.join(query_folder, query_filename)
    query_img = Image.open(query_path).convert("RGB")

    plt.subplot(1, top_k + 1, 1)
    plt.imshow(query_img)
    plt.title(f"QUERY\n{query_filename}", fontsize=10)
    plt.axis("off")

    # Retrieved gallery images
    for i, gallery_filename in enumerate(retrieved):
        gallery_path = os.path.join(gallery_folder, gallery_filename)
        gallery_img = Image.open(gallery_path).convert("RGB")

        plt.subplot(1, top_k + 1, i + 2)
        plt.imshow(gallery_img)
        plt.title(f"Rank {i+1}\n{gallery_filename}", fontsize=10)
        plt.axis("off")

    plt.tight_layout()
    plt.show()

